class I18n:
    def __init__(self, lang: str = "ja"):
        self.lang = lang

    def t(self, key: str, **kwargs) -> str:
        text = TRANSLATIONS.get(self.lang, TRANSLATIONS["ja"]).get(key, key)
        if kwargs:
            text = text.format(**kwargs)
        return text

    def set_lang(self, lang: str):
        self.lang = lang
